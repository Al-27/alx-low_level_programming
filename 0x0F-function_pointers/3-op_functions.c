#include "3-calc.h"

/**
 * op_add - desc
 * @a: param
 * @b: param
 *
 * Return: type
 */
int op_add(int a, int b)
{
	return (a+b);
}

/**
 * op_sub - desc
 * @a: param
 * @b: param
 *
 * Return: type
 */
int op_sub(int a, int b)
{
	return (a-b);
}

/**
 * op_mul - desc
 * @a: param
 * @b: param
 *
 * Return: type
 */
int op_mul(int a, int b)
{
	return (a*b);
}

/**
 * op_div - desc
 * @a: param
 * @b: param
 *
 * Return: type
 */
int op_div(int a, int b)
{
	return (a/b);
}

/**
 * op_mod - desc
 * @a: param
 * @b: param
 *
 * Return: type
 */
int op_mod(int a, int b)
{
	return (a%b);
}
